use std::num::ParseIntError;
use std::ptr;
use std::str::FromStr;
use substring::Substring;

enum Item {
    Num(i32),
    List(Vec<Item>)
}

impl Item {
    fn compare(&self, other: &Item) -> i32 {
        match (self, other) {
            (Item::Num(l), Item::Num(r)) => r-l,
            (l @ Item::List(_), Item::Num(r)) => l.compare(&Item::List(vec![Item::Num(*r)])),
            (Item::Num(l), r @ Item::List(_)) => Item::List(vec![Item::Num(*l)]).compare(r),
            (Item::List(l), Item::List(r)) => {
                for (li, ri) in l.iter().zip(r) {
                    let comp = li.compare(ri);
                    if comp != 0 { return comp;}
                }
                
                r.len() as i32 - l.len() as i32
            },
        }
    }
}

impl FromStr for Item {
    type Err = ParseIntError;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        if &s[0..1] == "[" && s.chars().last() == Some(']') {
            Ok(Item::List(hierarchical_split(s).iter().map(|i| i.parse::<Item>().unwrap()).collect()))
        } else {
            Ok(Item::Num(s.parse::<i32>().unwrap()))
        }
    }
}

fn hierarchical_split(s: &str) -> Vec<&str> {
    let mut start = 1;
    let mut nestings = 0;
    let mut results = Vec::new();
    for i in 1..s.len() - 2 {
        match &s[i..=i] {
            "[" => nestings += 1,
            "]" => nestings -= 1,
            "," => { 
                if nestings == 0 { 
                    results.push(s.substring(start, i)); 
                    start = i + 1;
                }
            },
            _ => ()
        }
    }
    
    if s.len() > 2 { 
        results.push(s.substring(start, s.len() - 1)); 
    }
    
    results
}


fn solve1(data: String) -> usize {
    let messages = parse(data);
    messages.into_iter().enumerate().filter(|(_, p)| p.0.compare(&p.1) > 0).map(|(i, _)| i + 1).sum()
}



fn solve2(data: String) -> usize {
    let messages = parse(data);
    let mut all = Vec::new();
    for (l, r) in messages.iter() {
        all.push(l);
        all.push(r);
    }
    let key1 = Item::List(vec![Item::List(vec![Item::Num(2)])]);
    let key2 = Item::List(vec![Item::List(vec![Item::Num(6)])]);
    all.push(&key1);
    all.push(&key2);
    
    all.sort_by(|a, b| b.compare(a).cmp(&0));
    (all.iter().position(|x| ptr::eq(*x, &key1)).unwrap() + 1) * (all.iter().position(|x| ptr::eq(*x, &key2)).unwrap() + 1)
}

fn parse(data: String) -> Vec<(Item, Item)> {
    data.split("\n\n").map(|line| line.split("\n")).map(|mut e| (e.next().unwrap().parse().unwrap(), e.next().unwrap().parse().unwrap())).collect()
}

